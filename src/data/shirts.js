// Função para buscar produtos
export const searchProducts = (query) => {
  const allProducts = getAllProducts();
  if (!query) return allProducts;
  
  return allProducts.filter(product =>
    product.name.toLowerCase().includes(query.toLowerCase()) ||
    (product.team && product.team.toLowerCase().includes(query.toLowerCase())) ||
    (product.country && product.country.toLowerCase().includes(query.toLowerCase())) ||
    (product.championship && product.championship.toLowerCase().includes(query.toLowerCase()))
  );
};

